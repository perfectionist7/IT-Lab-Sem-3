	AREA RESET,DATA,READONLY
	EXPORT __Vectors
	
__Vectors 
	DCD 0X10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R2,=DST
	MOV R3,#0
	MOV R4,#0
	MOV R5,#10
UP
	LDR R6,[R0],#4
	ADDS R3,R6
	ADCS R4,#0
	SUBS R5,#1
	BNE UP
	STR R3,[R2],#4
	STR R4,[R2]
STOP B STOP
SRC DCD 0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,0x9,0x10
	AREA DATA_SEG,READWRITE
DST DCD 0,0
	END