	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC1
	LDR R1,=SRC2
	LDR R5,=RES
	MOV R6,#4
UP
	LDR R2,[R0],#4
	LDR R3,[R1],#4
	SBCS R4,R2,R3
	STR R4,[R5],#4
	SUB R6,#1
	BNE UP
STOP B STOP
SRC1 DCD 0X741235FA,0XABCDEF12,0X98765432,0X56789034
SRC2 DCD 0XA145ADFC,0X12345678,0X11111111,0XAB123123
	AREA DATA_SEG,READWRITE
RES DCD 0
	END