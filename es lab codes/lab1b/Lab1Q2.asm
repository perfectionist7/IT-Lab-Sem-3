	AREA RESET,DATA,READONLY
	EXPORT __Vectors
	
__Vectors 
	DCD 0X10010000
	DCD Reset_Handler
	
	ALIGN
	AREA CODE_SEG, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC1
	LDR R1,=SRC2
	LDR R2,=DST
	MOV R3,#4
UP
	LDR R4,[R0],#4
	LDR R5,[R1],#4
	ADCS R4,R5
	STR R4,[R2],#4
	SUBS R3,#1
	BNE UP
STOP B STOP
SRC1 DCD 0x12345678,0x98765432,0xABCDEF12,0X56789012
SRC2 DCD 0X1,0X2,0X3,0X4
	AREA DATA_SEG,READWRITE
DST DCD 0,0,0,0
	END